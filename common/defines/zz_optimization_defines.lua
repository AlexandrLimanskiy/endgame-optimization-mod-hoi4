NDefines.NAI.UPGRADE_DIVISION_RELUCTANCE = 10                         -- vanilla: 7
NDefines.NAI.REFIT_SHIP_RELUCTANCE = 42                               -- vanilla: 28

-- ---------------------------------------------------------------------------
-- Рейды (система Raids, Graveyard of Empires+): лимиты переоценки целей
