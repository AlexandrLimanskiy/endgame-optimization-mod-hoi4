-- ============================================================================
-- Endgame Performance Optimization — simulation defines (HOI4 1.19.*)
-- ============================================================================
-- Принцип: трогаем ТОЛЬКО частоты пересчёта кэшей и фоновых переоценок ИИ,
-- т.е. то, что сами разработчики пометили "affects performance".
-- Ни один дефайн ниже не ограничивает возможности ИИ (число дивизий, заводов,
-- агрессию, качество шаблонов) — меняется лишь то, как часто движок
-- перестраивает внутренние структуры данных.
-- Все ванильные значения указаны в комментариях для прозрачности.
-- ============================================================================

-- ---------------------------------------------------------------------------
-- Торговля: полный пересчёт всех торговых маршрутов мира — одна из самых
-- дорогих периодических операций в эндгейме (сотни стран × конвои × пути).
-- Событийные пересчёты (изменение торговли, война, блокада) сохраняются,
-- меняется только период принудительного полного пересчёта.
-- ---------------------------------------------------------------------------
NDefines.NGame.TRADE_ROUTE_RECALCULATE_FREQUENCY_DAYS = 90            -- vanilla: 45

-- ---------------------------------------------------------------------------
-- Отрисовка линий фронта для стран ИИ (это чисто визуальная интерполяция,
-- у игрока остаётся полная). Vanilla уже использует упрощённую версию для ИИ.
-- ---------------------------------------------------------------------------
NDefines.NCountry.INTERPOLATED_FRONT_STEPS_SHORT = 1                  -- vanilla: 2

-- Скрытый пул "удалённого" снаряжения (после ручного удаления со склада)
-- хранится и учитывается меньше времени => меньше записей в стокпайлах.
NDefines.NCountry.NUM_DAYS_TO_FULLY_DELETE_STOCKPILED_EQUIPMENT = 45  -- vanilla: 90

-- Цикл опроса событий. В ванилле ~310 событий НЕ is_triggered_only:
-- их триггеры (у 59 — с циклами по всем странам/штатам, т.е. O(N^2))
-- перевычисляются для каждой страны и штата каждые X дней. Комментарий
-- Paradox: "1 is ideal, but CPU heavy". Реже опрос = флейвор-события
-- приходят чуть менее пунктуально, содержание игры не меняется.
NDefines.NCountry.EVENT_PROCESS_OFFSET = 30                           -- vanilla: 20

-- ---------------------------------------------------------------------------
-- Стратегический ИИ: кэши верхнего уровня. Помечены Paradox как
-- "affects performance". Это не тактические решения — реакция ИИ в бою
-- не меняется, меняется период обновления фоновых стратегических оценок.
-- ---------------------------------------------------------------------------
NDefines.NAI.AI_UPDATE_ROLES_FREQUENCY_HOURS = 96                     -- vanilla: 48  (роли стран: "агрессор/защитник" и т.п.)
NDefines.NAI.AI_NAVAL_GOALS_UPDATE_FREQUENCY_DAYS = 12                -- vanilla: 7   (генерация морских целей)
NDefines.NAI.UPDATE_SUPPLY_BOTTLENECKS_FREQUENCY_HOURS = 336          -- vanilla: 168 (поиск узких мест снабжения: раз в 2 недели вместо 1)
NDefines.NAI.UPDATE_SUPPLY_MOTORIZATION_FREQUENCY_HOURS = 168         -- vanilla: 52  (проверка "поможет ли моторизация хабу")

-- Force Concentration (AIFC) — планировщик наступлений. Paradox:
-- "Lowering this number may decrease performance" => слегка повышаем.
NDefines.NAI.AIFC_UPDATE_FREQUENCY_DAYS = 7                           -- vanilla: 5
NDefines.NAI.AIFC_UNIT_NUDGE_FREQUENCY_DAYS = 20                      -- vanilla: 15 (перетасовка дивизий внутри одного фронта)

-- Рейды и переоценка морских регионов: пересчёт целей — это перебор
-- регионов × стран; сами миссии продолжают выполняться непрерывно.
NDefines.NAI.RAIDS_CREATE_FREQUENCY_DAYS = 14                         -- vanilla: 7
NDefines.NAI.STRIKE_FORCE_TARGET_RECALC_DAYS = 8                      -- vanilla: 5
NDefines.NAI.CONVOY_RAIDING_TARGET_RECALC_DAYS = 20                   -- vanilla: 15
NDefines.NAI.AI_OBJECTIVE_DEFAULT_TARGET_RECALC_DAYS = 8              -- vanilla: 5

-- Авиаприоритеты: раз в неделю вместо раза в 4 дня. Назначенные миссии
-- летают каждый час как обычно — меняется только период перераспределения.
NDefines.NAI.DAYS_BETWEEN_AIR_PRIORITIES_UPDATE = 7                   -- vanilla: 4

-- Веса исследований и рынок вооружений: чистые таблицы приоритетов.
NDefines.NAI.RESEARCH_DAYS_BETWEEN_WEIGHT_UPDATE = 14                 -- vanilla: 7
NDefines.NAI.EQUIPMENT_MARKET_UPDATE_FREQUENCY_DAYS = 16              -- vanilla: 11

-- ---------------------------------------------------------------------------
-- Сборка мусора в данных ИИ: устаревшие неиспользуемые шаблоны дивизий
-- удаляются в 4 раза быстрее. Каждый живой шаблон участвует в матчинге
-- производства/апгрейдов у КАЖДОЙ страны — к 1944 их накапливаются сотни.
-- ---------------------------------------------------------------------------
