NDefines.NRaids.MAX_TARGETS_TO_UPDATE_PER_FRAME = 50                  -- vanilla: 100 (частота обновления иконок на карте)

-- ---------------------------------------------------------------------------
-- Потолок дивизий ИИ (пресет "Сбалансированный: 250").
-- WANTED_UNITS_* управляет тем, сколько дивизий ИИ ХОЧЕТ иметь: сверх
-- потолка он просто не тренирует новые. Существующие дивизии никогда
-- не удаляются, игрока эта логика не касается вовсе (это чисто ИИ-расчёт),
-- потолок один для всех ИИ — относительный баланс сил сохраняется.
